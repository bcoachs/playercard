export function getCountryCode(value: string | null | undefined): string | null {
  if (!value) return null
  const trimmed = value.trim()
  if (!trimmed) return null
  const upper = trimmed.toUpperCase()
  if (/^[A-Z]{2}$/.test(upper) && COUNTRY_LABEL_BY_CODE.has(upper)) {
    return upper
  }
  const normalized = normalizeValue(trimmed)
  const fromNormalized = COUNTRY_CODE_BY_NORMALIZED_LABEL.get(normalized)
  if (fromNormalized) return fromNormalized
  const fallback = ALT_LABEL_TO_CODE[normalized.replace(/\s+/g, '')]
  if (fallback) return fallback
  return null
}

export function getCountryLabel(value: string | null | undefined): string | null {
  const code = getCountryCode(value)
  if (!code) return null
  return COUNTRY_LABEL_BY_CODE.get(code) ?? null
}
